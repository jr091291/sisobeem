package sisobeem.artifacts;

/**
 * Clase Coordenada. 
 * @author Erley
 *
 */
public class Coordenada {
   int x;
   int y;
   
   public Coordenada(int x, int y){
	   this.x = x;
	   this.y = y;
   }
  
  /**** Metodos Accesores *****/
   
  public int getX(){
	  return this.x;
  }
  
  public int getY(){
	  return this.y;
  }
  
}
